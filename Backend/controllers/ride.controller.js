const rideService = require("../services/ride.service");
const { validationResult } = require("express-validator");
const mapService = require("../services/map.service");
const { sendMessageToSocketId } = require("../socket");
const rideModel = require("../models/ride.model");
const userModel = require("../models/user.model");
const captainModel = require("../models/captain.model");

const dispatchToNextCaptain = async (rideId) => {
  try {
    const ride = await rideModel.findById(rideId).populate("user");
    if (!ride || ride.status !== "pending") return;

    const nextIndex = ride.currentCaptainIndex + 1;
    if (nextIndex >= ride.availableCaptains.length) {
      ride.status = "cancelled";
      await ride.save();
      sendMessageToSocketId(ride.user.socketId, {
        event: "ride-cancelled",
        data: ride,
      });
      return;
    }

    ride.currentCaptainIndex = nextIndex;
    await ride.save();

    const nextCaptainId = ride.availableCaptains[nextIndex];
    const nextCaptain = await captainModel.findById(nextCaptainId);

    if (nextCaptain && nextCaptain.socketId) {
      const sent = sendMessageToSocketId(nextCaptain.socketId, {
        event: "new-ride",
        data: ride,
      });

      if (sent) {
        // 30 seconds wait for each captain
        setTimeout(() => dispatchToNextCaptain(rideId), 30000);
      } else {
        // Socket connection is dead, skip to next immediately
        dispatchToNextCaptain(rideId);
      }
    } else {
      // Captain offline, skip to next immediately
      dispatchToNextCaptain(rideId);
    }
  } catch (error) {
    console.error("Dispatch error:", error);
  }
};

module.exports.chatDetails = async (req, res) => {
  const { id } = req.params;
  try {
    const ride = await rideModel
      .findOne({ _id: id })
      .populate("user", "socketId fullname phone")
      .populate("captain", "socketId fullname phone");

    if (!ride) {
      return res.status(400).json({ message: "Ride not found" });
    }

    const response = {
      user: {
        socketId: ride.user?.socketId,
        fullname: ride.user?.fullname,
        phone: ride.user?.phone,
        _id: ride.user?._id,
      },
      captain: {
        socketId: ride.captain?.socketId,
        fullname: ride.captain?.fullname,
        phone: ride.captain?.phone,
        _id: ride.captain?._id,
      },
      messages: ride.messages,
    };

    res.status(200).json(response);
  } catch (error) {
    console.error(error);
    res.status(500).json({ message: "Server error" });
  }
};

module.exports.createRide = async (req, res) => {
  const errors = validationResult(req);
  if (!errors.isEmpty()) {
    return res.status(400).json({ errors: errors.array() });
  }

  const { pickup, destination, vehicleType } = req.body;

  try {
    const ride = await rideService.createRide({
      user: req.user._id,
      pickup,
      destination,
      vehicleType,
    });

    const user = await userModel.findOne({ _id: req.user._id });
    if (user) {
      user.rides.push(ride._id);
      await user.save();
    }

    res.status(201).json(ride);

    Promise.resolve().then(async () => {
      try {
        const pickupCoordinates = await mapService.getAddressCoordinate(pickup);
        console.log("Pickup Coordinates", pickupCoordinates);

        const captainsInRadius = await mapService.getCaptainsInTheRadius(
          pickupCoordinates.ltd,
          pickupCoordinates.lng,
          10, // Increased radius to ensure finding the absolute nearest
          vehicleType
        );

        // Sort by Euclidean distance to find the true nearest driver
        const sortedCaptains = captainsInRadius.sort((a, b) => {
          const distA = Math.pow(a.location.coordinates[0] - pickupCoordinates.lng, 2) + Math.pow(a.location.coordinates[1] - pickupCoordinates.ltd, 2);
          const distB = Math.pow(b.location.coordinates[0] - pickupCoordinates.lng, 2) + Math.pow(b.location.coordinates[1] - pickupCoordinates.ltd, 2);
          return distA - distB;
        });

        if (sortedCaptains.length === 0) return; // Add error handling later if no captains at all

        ride.availableCaptains = sortedCaptains.map(c => c._id);
        ride.currentCaptainIndex = 0;
        await ride.save();

        const rideWithUser = await rideModel.findOne({ _id: ride._id }).populate("user");

        const firstCaptain = sortedCaptains[0];

        if (firstCaptain.socketId) {
          const sent = sendMessageToSocketId(firstCaptain.socketId, {
            event: "new-ride",
            data: rideWithUser,
          });

          if (sent) {
            setTimeout(() => dispatchToNextCaptain(rideWithUser._id), 30000);
          } else {
            dispatchToNextCaptain(rideWithUser._id);
          }
        } else {
          dispatchToNextCaptain(rideWithUser._id);
        }

      } catch (e) {
        console.error("Background task failed:", e.message);
      }
    });
  } catch (err) {
    return res.status(500).json({ message: err.message });
  }
};

